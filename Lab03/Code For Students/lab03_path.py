def graph_has_a_path(graph, start, end):
    # TODO: 
    visitados = [False] * len(graph)
    visitados[start] = True
    cola_nodos = [start]
    while cola_nodos:
        primer_nodo = cola_nodos.pop(0)
        for vecino in range(len(graph[0])):
            if (graph[primer_nodo][vecino] == 0 or visitados[vecino]):
                continue
            if (vecino == end):
                return True
            visitados[vecino] = True
            cola_nodos.append(vecino)

   
  
